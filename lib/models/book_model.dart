class Book {
  final String id;
  final String title;
  final String author;
  final double price;
  final String category;
  final double rating;
  final String coverUrl;

  Book({
    required this.id,
    required this.title,
    required this.author,
    required this.price,
    required this.category,
    required this.rating,
    required this.coverUrl,
  });
}
